doc.add_heading('1.1 研究背景', 2)
doc.add_paragraph('现在，我们云南民族大学有很多学生。大家每天都要去图书馆学习。所以，图书馆的座位经常不够用。这是一个非常大的学生痛点。传统的占座办法是谁先到谁先得。这个落后的办法很容易让人吵架。然后，学生也不能提前用手机看到座位空不空 (Hu 156-158)。')
doc.add_paragraph('在这个时候，我们有了很多新的写代码的技术。很多大学已经开始用手机系统解决抢座位的问题 (Zhang and Lin 89-91)。我看到Python这门语言开发这种小系统很简单。另外，大家都有微信。所以，我觉得我们可以用新技术给这所大学做一个微信抢座系统。我不清楚其他学校底层的代码是怎么写的，所以我自己从头设计了一个架构。')

doc.add_heading('1.2 目的意义', 2)
doc.add_paragraph('我做这个系统有两个很直接的目的。首先是实践目的。我想让找座位变得很简单。学生不需要去图书馆跑来跑去。学生用手机就可以看到一楼和二楼的空座位。然后，学生可以直接定下来。另外，我想帮图书馆的老师减轻负担。老师不需要每天盯着座位看 (Li and Zhang 67-69)。然后，系统会自动对占座不来的同学扣分。')
doc.add_paragraph('第二是理论目的。我想练习我的写代码能力。我用来写毕业设计的技术很流行。这对我自己以后找工作来说很有意义。')

