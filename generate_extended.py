doc.add_heading('3.2 系统的使用者需求', 2)
doc.add_paragraph('我把系统分成了两类需求。第一类是学生的需求。学生需要一个登录的页面。学生需要能看到热门座位的图片。然后，学生需要点座位进行预约。预约好之后，学生需要一个按钮可以签到。另外，如果不去，学生需要可以取消。这就是学生的全部需求。')
doc.add_paragraph('第二类是管理员的需求。管理员也就是老师。老师不用小程序，老师用电脑网页。老师需要看到所有人谁犯了规。如果不签到，老师需要看到系统扣了分。另外，老师需要用图表看每天什么时候人最多。这种图表帮忙老师管理 (Cao 78-80)。')

doc.add_heading('第四章 系统设计', 1)

doc.add_heading('4.1 总体架构设计', 2)
doc.add_paragraph('我整体采用了B/S结构。意思就是浏览器和服务器分开。微信小程序就是那个浏览器。然后Flask后端的代码就是服务器。我负责把前后两端连起来。小程序通过网络向后端发送请求。后端查好了数据再通过网络发回给小程序。这种设计很清楚。而且这样写代码不容易乱。')

doc.add_heading('4.2 数据库表格设计', 2)
doc.add_paragraph('为了保存所有的信息，我设计了几张简单的表。第一张是用户表。这个表存放学生的学号、姓名、和密码。第二张是座位表。这张表存座位的编号，以及它是空着还是被占了。第三张是预约记录表。只要有学生预约，这个表就多一行字。并且这张表记录了开始时间和结束时间。我不懂太复杂的外键关联约束。所以我尽量把表格设计成独立的。这样不容易出严重错误。')

doc.add_heading('4.3 信用积分扣分规则', 2)
doc.add_paragraph('这是一个很核心的规则设计。由于占座现象严重，我设计了积分机制。每一个刚进系统的学生默认有100分。如果学生预约了座位，但是到了时间没去扫码签到。系统就自动扣这名学生20分。然后，如果他的分数低于60分。系统就不准他再预约座位。这个扣分办法可以很好地保护座位不被浪费。这其实是一个很基础的惩罚规定。')

