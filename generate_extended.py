doc.add_heading('2.1 Python和Flask框架', 2)
doc.add_paragraph('我首先选了Python这门语言写后端。Python语言很好写代码。Python里面有很多现成的工具可以直接下载用 (Zhang et al. 98-100)。然后，我用了Flask框架。我不知道Flask底层的复杂原理。我也不懂服务器深层的请求解析。但是，我知道Flask框架可以很快地搭出一个后台网站。Flask代码很少。所以，我选了这些工具写核心逻辑。')

doc.add_heading('2.2 微信小程序前端', 2)
doc.add_paragraph('前端界面部分，我用了微信小程序做手机上的界面。由于我不知道怎么开发独立的苹果和安卓App，我放弃了写独立App。学生每天都用微信聊天。小程序不需要安装。学生打开微信就可以扫码签个到。学生不需要再去下载一个新的软件。这非常方便 (He and Qin 101-103)。小程序里可以画座位图。')

doc.add_heading('2.3 数据库MySQL和Redis', 2)
doc.add_paragraph('对于存储数据，我选了MySQL存放我们的数据记录。MySQL有很好的表格。MySQL可以稳定地存放很多学生账号信息。MySQL也可以存放几千个座位的信息。由于并发量大的时候MySQL会卡，然后，我也用了Redis技术。Redis是一个缓存在系统内存里的工具。我不清楚Redis底层哈希树的结构。但是在这个时候，Redis可以让查空座位的速度变得非常快。所以，早上一百个同学抢位置的时候就不会卡顿。')

