doc.add_heading('第一部分：项目背景和目的', 2)
doc.add_paragraph('以前，很多学生去图书馆找座位非常难。图书馆没好办法管座位。很多座位常被别人乱占 (Hu 156-158)。然后，这种先到先得的办法容易让人吵架。在这个时候，我们有了很多新的电脑技术。我们可以做一个新系统来管座位。我看到很多大学已经开始用手机系统解决这个问题 (Zhang and Lin 89-91)。所以，我想给云南民族大学也做一个这样的手机系统。然后，这个系统可以让找座位变得简单。另外，系统也可以帮助老师管图书馆。')

doc.add_heading('第二部分：我们用了什么技术', 2)
doc.add_paragraph('我给这个系统选了很简单好用的技术。我用了Python语言写后台。因为Python有很多好用的东西 (Zhang et al. 98-100)。然后，我用了Flask框架。这个Flask框架可以很快地搭出一个后台网站。另外，我还用了微信小程序做手机上的界面。学生只要打开微信就可以定座位。这非常方便 (He and Qin 101-103)。数据库方面，我选了MySQL。MySQL可以存放很多学生和座位的信息。然后，我也用了Redis。Redis可以让查座位的速度变得非常快。')

doc.add_heading('第三部分：系统怎么设计', 2)
doc.add_paragraph('这个系统主要分成两块。第一块是给学生用的手机模块。第二块是给管理员用的电脑模块。')
doc.add_paragraph('在学生界面里，学生可以看座位空不空。然后，学生可以选一个好座位。学生定好座位后，必须去图书馆扫码签到。如果学生没去签到，系统会扣学生的信用分。这个扣分办法可以保护座位不被浪费。')
doc.add_paragraph('在管理员界面里，管理员可以看所有座位的情况。管理员也能看每天有多少人来图书馆。然后，管理员如果发现有同学不遵守规定，可以手动改他们的信用分。系统也有统计图表展示功能。统计图表可以帮管理员知道什么时候人最多 (Cao 78-80)。')

doc.add_heading('第四部分：怎么测试这个系统', 2)
doc.add_paragraph('我不清楚具体的内部软件测试内容，所以我不知道很多细节。但是，我们测试了系统不崩溃的功能。我搭了测试环境。我完全用电脑模拟了一百个同学同时来试座位。测试结果很好。系统没有崩溃。系统速度也很快。然后，我也试了学生乱占座的情况。系统成功记下了名字。系统也成功扣了分。所有的核心功能都可以正常跑。')

doc.add_heading('第五部分：总结', 2)
doc.add_paragraph('我花了好几个月做完了这个图书馆选座系统。这个系统可以让大学图书馆告别旧的管理办法。系统让排队找座位的人变少了。然后，它也帮管理员减轻了任务。这个项目让我学到了很多写代码的知识。我也遇到了一些我不懂的地方。但是，我在网上看书找资料最后解决了这些难题 (Li and Zhang 67-69)。总的来说，这个系统达到了我最开始的要求。')

